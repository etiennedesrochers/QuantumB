def available_languages() -> dict[str, str]:
    """Return {lang_code: display_name} for all available languages."""
    return {"en": "English", "fr": "Fran\u00e7ais"}


def get_language() -> str:
    """Return the current language code."""
    return _CURRENT_LANG


def set_language(lang: str) -> None:
    """Set the current language. No-op if lang is not in TRANSLATIONS."""
    global _CURRENT_LANG
    if lang in TRANSLATIONS:
        _CURRENT_LANG = lang


def tr(key: str, **kwargs: str) -> str:
    """Return the translated string for key in the current language.

    Falls back to English if the key is missing in the current language.
    Falls back to the key itself if missing in English too.
    """
    text = TRANSLATIONS.get(_CURRENT_LANG, {}).get(key) or TRANSLATIONS["en"].get(key, key)
    return text.format(**kwargs) if kwargs else text
